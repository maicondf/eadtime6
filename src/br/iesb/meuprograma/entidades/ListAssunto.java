public class ListAssunto {
    private int list_id;
    private String list_assunto;
    private String list_situacao;

    public int getList_id() {
        return list_id;
    }

    public void setList_id(int list_id) {
        this.list_id = list_id;
    }

    public String getList_assunto() {
        return list_assunto;
    }

    public void setList_assunto(String list_assunto) {
        this.list_assunto = list_assunto;
    }

    public String getList_situacao() {
        return list_situacao;
    }

    public void setList_situacao(String list_situacao) {
        this.list_situacao = list_situacao;
    }
    
}